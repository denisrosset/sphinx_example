"""
This modules defines arithmetic functions.
"""

__version__ = "0.0.1"


def plus(x: float, y: float) -> float:
    """
    Adds two numbers

    Args:
        x: First number
        y: Second number

    Returns:
        The sum
    """
    return x + y


def times(x: float, y: float) -> float:
    """
    Multiplies two numbers

    Args:
        x: First number
        y: Second number

    Returns:
        The sum
    """
    return x * y
